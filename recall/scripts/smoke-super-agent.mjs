#!/usr/bin/env node
/**
 * End-to-end Super Agent smoke: AssemblyAI transcription (sample MP3) + LLM Gateway
 * (same service calls as meeting-super-agent-complete), then prints UI-shaped output
 * and optionally writes an HTML preview.
 *
 *   cd recall && node scripts/smoke-super-agent.mjs
 *   cd recall && railway run -s "Meeting Assistant V1" node scripts/smoke-super-agent.mjs
 *
 * Options:
 *   --html <path>   Write a meeting-detail-style HTML preview (open in browser).
 */

import "dotenv/config";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const SAMPLE_AUDIO_URL =
  process.env.SUPER_AGENT_SMOKE_AUDIO_URL ||
  "https://storage.googleapis.com/aai-web-samples/5_common_sports_injuries.mp3";

const POLL_MS = 2500;
const DEADLINE_MS = 180_000;

function parseArgs() {
  const args = process.argv.slice(2);
  const out = { html: null };
  for (let i = 0; i < args.length; i++) {
    if (args[i] === "--html" && args[i + 1]) {
      out.html = args[++i];
    }
  }
  return out;
}

function esc(s) {
  if (s == null) return "";
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function renderHtml({ summaryResult, transcript, metadataTitle }) {
  const chapters = Array.isArray(transcript.chapters) ? transcript.chapters : [];
  const actions = summaryResult.actionItems || [];
  const decisions = summaryResult.decisions || [];
  const highlights = summaryResult.highlights || [];

  const chapterBlocks = chapters.length
    ? chapters
        .map((c) => {
          const h = esc(c.headline || c.gist || "Chapter");
          const sum = esc(c.summary || "");
          return `<div class="card"><div class="h">${h}</div><p class="s">${sum}</p></div>`;
        })
        .join("")
    : '<p class="muted">No chapters</p>';

  const li = (items, key) =>
    items.length
      ? `<ul>${items
          .map((item) => {
            const text =
              item?.task ||
              item?.decision ||
              item?.title ||
              item?.summary ||
              JSON.stringify(item);
            return `<li>${esc(text)}</li>`;
          })
          .join("")}</ul>`
      : `<p class="muted">No ${key}</p>`;

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8"/>
  <title>Super Agent smoke preview</title>
  <style>
    body { font-family: system-ui, sans-serif; max-width: 48rem; margin: 2rem auto; padding: 0 1rem; color: #111; }
    h1 { font-size: 1.25rem; }
    h2 { font-size: 1rem; margin-top: 1.5rem; border-bottom: 1px solid #e5e7eb; padding-bottom: 0.25rem; }
    .pill { display: inline-block; padding: 0.2rem 0.6rem; border-radius: 9999px; background: #ecfdf5; color: #166534; font-size: 0.75rem; }
    .card { border: 1px solid #e5e7eb; border-radius: 0.375rem; padding: 0.75rem; margin: 0.5rem 0; }
    .card .h { font-weight: 600; }
    .card .s { font-size: 0.875rem; color: #4b5563; margin-top: 0.25rem; }
    .summary { white-space: pre-wrap; font-size: 0.875rem; line-height: 1.5; }
    .muted { color: #6b7280; font-size: 0.875rem; }
    ul { font-size: 0.875rem; padding-left: 1.25rem; }
  </style>
</head>
<body>
  <p class="pill">Completed · Super Agent smoke</p>
  <h1>${esc(metadataTitle)}</h1>
  <p class="muted">Generated by <code>scripts/smoke-super-agent.mjs</code> — same LLM path as production.</p>

  <h2>Detailed Summary</h2>
  <div class="summary">${esc(summaryResult.detailedSummary || "")}</div>

  <h2>Chapters</h2>
  ${chapterBlocks}

  <h2>Action Items</h2>
  ${li(actions, "action items")}

  <h2>Decisions</h2>
  ${li(decisions, "decisions")}

  <h2>Highlights</h2>
  ${li(highlights, "highlights")}
</body>
</html>`;
}

async function main() {
  const { html: htmlPath } = parseArgs();
  const API_KEY = process.env.ASSEMBLYAI_API_KEY?.trim();
  if (!API_KEY) {
    console.error("ASSEMBLYAI_API_KEY is not set. Use recall/.env or:");
    console.error('  railway run -s "Meeting Assistant V1" node scripts/smoke-super-agent.mjs');
    process.exit(1);
  }

  const mod = await import("../services/assemblyai/index.js");
  const AssemblyAI = mod.default;

  console.log("Super Agent smoke test");
  console.log("  Audio:", SAMPLE_AUDIO_URL);
  console.log("");

  const requestBody = {
    auto_chapters: true,
    speaker_labels: true,
  };

  console.log("Submitting transcription (with chapters)…");
  const { transcript: created } = await AssemblyAI.submitTranscript({
    audioUrl: SAMPLE_AUDIO_URL,
    requestBody,
    allowUploadFallback: true,
  });
  const transcriptId = created?.id;
  if (!transcriptId) {
    console.error("No transcript id returned");
    process.exit(1);
  }
  console.log("  Transcript id:", transcriptId);

  let status = created.status;
  let transcript = created;
  const deadline = Date.now() + DEADLINE_MS;
  while (status !== "completed" && status !== "error" && Date.now() < deadline) {
    await new Promise((r) => setTimeout(r, POLL_MS));
    transcript = await AssemblyAI.getTranscript(transcriptId);
    status = transcript.status;
    process.stdout.write(`  status: ${status}\r`);
  }
  console.log("");

  if (status === "error") {
    console.error("Transcription error:", transcript.error || transcript);
    process.exit(1);
  }
  if (status !== "completed") {
    console.error("Timeout, last status:", status);
    process.exit(1);
  }

  const transcriptText = AssemblyAI.getTranscriptPlainText(transcript);
  if (!transcriptText || transcriptText.length < 20) {
    console.error("Transcript text too short:", transcriptText?.length);
    process.exit(1);
  }
  console.log("Transcription OK:", transcriptText.length, "chars,", (transcript.chapters || []).length, "chapters");

  const metadataTitle = "Smoke test meeting · sports injuries sample";
  const metadata = {
    title: metadataTitle,
    participants: [{ name: "Sample" }],
    startTime: null,
  };

  console.log("Running Super Agent LLM (generateSuperAgentSummary)…");
  const summaryResult = await AssemblyAI.generateSuperAgentSummary({
    transcriptText,
    metadata,
    chapters: transcript.chapters || [],
  });

  console.log("\n========== MEETING PAGE (text preview) ==========\n");
  console.log("[Status pill] Completed\n");
  console.log("--- Detailed Summary ---\n");
  console.log((summaryResult.detailedSummary || "").trim() || "(empty)");
  console.log("\n--- Chapters ---");
  for (const c of transcript.chapters || []) {
    console.log("-", c.headline || c.gist || "?", "—", (c.summary || "").slice(0, 120));
  }
  if (!(transcript.chapters || []).length) console.log("(none)");
  console.log("\n--- Action Items ---");
  for (const a of summaryResult.actionItems || []) {
    console.log("-", a.task || a.text || JSON.stringify(a));
  }
  if (!(summaryResult.actionItems || []).length) console.log("(none)");
  console.log("\n--- Decisions ---");
  for (const d of summaryResult.decisions || []) {
    console.log("-", d.decision || d.text || JSON.stringify(d));
  }
  if (!(summaryResult.decisions || []).length) console.log("(none)");
  console.log("\n--- Highlights ---");
  for (const h of summaryResult.highlights || []) {
    console.log("-", h.title || "Highlight", "—", (h.summary || h.content || "").slice(0, 100));
  }
  if (!(summaryResult.highlights || []).length) console.log("(none)");
  console.log("\n========== end ==========\n");

  if (htmlPath) {
    const abs = path.isAbsolute(htmlPath) ? htmlPath : path.join(process.cwd(), htmlPath);
    const dir = path.dirname(abs);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(abs, renderHtml({ summaryResult, transcript, metadataTitle }), "utf8");
    console.log("Wrote HTML preview:", abs);
    console.log("Open that file in a browser to see the same blocks as the meeting Super Agent tab.");
  }

  console.log("Super Agent smoke completed successfully.");
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
